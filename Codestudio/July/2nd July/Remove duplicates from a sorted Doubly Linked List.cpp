/**
 * Definition of doubly linked list:
 * class Node {
 * public:
 *      int data;
 *      Node *prev;
 *      Node *next;
 *      Node() {
 *          this->data = 0;
 *          this->prev = NULL;
 *          this->next = NULL;
 *      }
 *      Node(int data) {
 *          this->data = data;
 *          this->prev = NULL;
 *          this->next = NULL;
 *      }
 *      Node (int data, Node *next, Node *prev) {
 *          this->data = data;
 *          this->prev = prev;
 *          this->next = next;
 *      }
 * };
 *
 *************************************************************************/

Node * removeDuplicates(Node *head)
{
    // Write your code here
    // Initialize 'cur' to store head
    Node * cur = head;

    // Run a while loop
    while(cur != NULL && cur->next!= NULL){
        if(cur->next->data == cur->data){
            cur->next = cur->next->next;
        }
        else{
            cur = cur->next;
        }
    }
    return head;


}
